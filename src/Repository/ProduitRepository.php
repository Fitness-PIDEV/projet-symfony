    /**
     * recupere les annonces en lien avec recherche
     * @return Produit[]
     */
    public function findSearch(SearchDataRec $search):array
    {
        $query= $this
            ->createQueryBuilder('x');


        if (!empty($search->y))
        {
            $query=$query
                ->andWhere('x.nomProduit LIKE :y ')
                ->setParameter('y',"{$search->y}%");
        }

        return $query->getQuery()->getResult();
    }

